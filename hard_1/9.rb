def egyptian(rational)
  fractions = []
  i = 1
  until fractions.sum == rational
    if fractions.sum + Rational(1, i) <= rational
      fractions << Rational(1, i)
    end
    i += 1
  end
  fractions.map { |fraction| fraction.denominator }
end

def unegyptian(denominators)
  denominators.map { |denominator| Rational(1, denominator) }.sum
end

